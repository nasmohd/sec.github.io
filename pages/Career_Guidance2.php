                     <h4> Life After Academics</h4>
                     <div class="text-justify" style="font-size:15px;">
                         We have seen all the academics journey in academics, however to be realists, some students leave school in;
<ul>
<li>earliest stages,</li>
<li>middle stages and others</li>
<li>Last stages.</li>
</ul>
Generally in <strong>early stages of academics(primary school)</strong>, all pupils (boys and girls), if they leave school it's due to traditions and customs like skipping schools for grazing in Inter-Lacustrine regions like in Mwanza, Musoma and in Northern Maasai Land due to negligence of education priority over traditions, none should leave school due to poverty since eduction is provided free in this level.
In the <strong>middle stages of academics (secondary education)</strong>, factors change a bit and increase a bit. Major factors are biological and economical. Biologically due to adolescence, some girls leave schools due to impregancement, either in raping cases, prostitutions or any sexual haaraasments while some boys become notorious to adhere to schools regulations and engage in street banditisms like robbery, ending in jails and fugitive life-styles.<br/><br/>
Financially, a bit increase in school fees especially science subjects in A level, single parent livelihood, ruralism contribute to quitting of schools.
 In the last <strong>stages of academics(Higher Learning Institutions)</strong>, major factors become Academical Excellence and Financial. to pursue in academics, elligibity matters the most, failure to adheherence with perfomance, a candidate if off the system's database automatically. However due to financial factors, the increase in tuition fees does give a way for failure to adheherence to quict the univeristy, at least Tamzamian government, has a special institute for loans named as HESLB, Higher Education Student's Loans Board on which benefficiaries get some support evne to Millions of their tuition fee, acomodattions, projects fee and few more.
<br/>
<br/>
However We are not all blessed in academics, <strong>"Everybody is a genius, but if you judge a fish by its ability to climb a tree, it will live its whole life believing its stupid." Abert Einstein, The Best Theoretical Physist of all times</strong>. He meant we are all blessed differently, some in academics, others in military, business, artists in musics, comedians, drawings, sports, e.t.c. so outside and after acdemics, its a talent and social skills that will help you survive. because earning money for basic needs like food, clothing, shelter, relaxation, is all we meet.
                         
