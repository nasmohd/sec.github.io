            <h4>Higher Learning Institutions</h4>
            <div class="text-justify" style="font-size:15px;" id="primary">
It is divided into three levels;
<ul>
<li>Under-Graduate Level </li>
<li>Graduate Level, and</li>
<li>Post Graduate Level.</li>
</ul>

This Level prepares Proffesionals who will come back and work in ordinary life in the society. This academic level is achieved by students either after A'Level or after O'Level;<br/><br/>
<strong>Under-Graduate Level</strong> <br/>
<strong>After O'Level: </strong><br/>
If candidates of Form Four after Form Four Results have poor results and are not willing to re-seat, can wait for applications to higher institutions then apply for the choices of their passions like technicians, artists or econimists, or one can decide to do any short course or start business to establish oneself in life. <br/><br/>
If the students after form Four results has passed but unwilling for A'Level, one can choose to wait for applications to higher institutionss then apply for the choices of their passions like technicians, artists or econimists, or wait for selections of form five and Universties if the student did apply for university or pursue with business inthe street to establish their life. For Student from O Level, upon joining Higher Learning Institutions, they do begin with Certificates to Ordinary Diploma, for technicians they do have National Technical Award Levels, NTA Level 4-6, (NTA Level 4 -Certificate, NTA Level 5 -Basic Technician Certificate and NTA Level 6 - Full Technician Certificate which is the Ordinary Diploma in particular field.) Non-Technicians do have 2 years For Diploma 1 and Diploma 2. Upon completing 2 year the student is being awarded Ordinary Diploma in particular field of study. <br/><br/>

<strong>After A'Level:</strong> <br/>
If candidate after Form 6 Results did not have enough credits to join Bachelor Degree, one will wait for applications to higher institutions then apply for the choices of their passions, the student will continue studies for 2 or 3 academic years and upon which completion, they will be rewarded an Ordinary Diploma in aparticular field of study.<br/><br/>

<strong>Graduate Level</strong><br/>
All efforts until Diploma will result in an Undergraduate student. Further academic pursue will result in a Graduate student, who will have Advanced Diploma and A Bachelor Degree Certificate. To obtain a Bachelor Degree, a ctudent will have to pass through 3 years for Social Economic Proffessions like Bachelor Degree in Arts,like Education, e.t.c., 4 years for a Bachelor Degree in Engineering, Computer Engineering, e.t.c. and 5 years for Bachelor Degree in Medical Sciences like Medical Doctors, e.t.c.<br/><br/>

<strong>Post Graduate Level</strong><br/>
A Bachelor Degree holder from any field(Arts, Engineering, Social Sciences, or Medical and Allied Sciences), if continues with academics pursuings, upon completion will be rewarded a Master Degree in a particular field of study. Like Masters of Science in Computertational Sciences. If the Master student further pursue academics in a particular field, upon completion will be rewarded a Doctorate of Philosophy in a particular field under pursue, coommonly referred to as <strong>PhD</strong>. If a PhD holder, still pursue academical exellence in a particular field, upon completion will be rewarded a Proffessor Degree. This is the limit of study ina particular field, if one seeks more excellence should chang the field of exploration and purue another one.
