            <h4>Secondary Education</h4>
            <div class="text-justify" style="font-size:15px;" id="secondary">
The next stage, after Primary Education is Secondary Education. it lasts for 6 years, but it has two phases,
<ul>
<li>Ordinary Level and</li>
<li>Advanced Level</li>
</ul>
Ordinary Level, commonly known as <strong>"O'Level"</strong>, the period duration is 4 years, First year for Form One, Second year for Form Two.
At form two, there is a National Examination, from National Examination Council of Tanzania (NECTA). Failures must re-do the year, while Survivors proceed with the Third year as Form Three, and the Fourth year as Form Four.

At form four, there is a a National Examination, from National Examination Council of Tanzania (NECTA). Failures must re-do the year, the common term is <strong>"Re-Seating"</strong> while Survivors proceed with the Fifth year as Form Five.
<br/><br/>
<strong>N.B.</strong>
At form three, most of Tanzanian Secondary schools, do give their students, options for Next Academics Progress, as laying down the Foundation for their future carriers in their Life. Options are three namely;
<ul>
<li>Science</li>
<li>Arts</li>
<li>Business</li>
</ul>
Arts Students are doing all 7 basic Subjects namely,Biology, Basic Mathematics, Geography, Kiswahili, English Language, Civics and History.
Business Students are doing all 7 basic Subjects namely,Biology, Basic Mathematics, Geography, Kiswahili, English Language, Civics and History. in addition to <strong>Bookkeeping</strong> and <strong>Commerce</strong>.<br/>
